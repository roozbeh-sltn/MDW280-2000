
public class CalculatriceSimple {
	
	public int addition(int a , int b) {
		
		return a+b;
	}
	
	public int division (int a , int b) {
		
		return a/b;
	}

	public int multiplication(int a ,int b) {
		
		return a*b;
		
	}
	
	public int sustraction (int a , int b) {
		
		return a-b;
	}
	
	public double cercle (double r) {
		
		return r * Math.PI;
	}
	
}

